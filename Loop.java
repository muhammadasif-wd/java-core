
public class Loop {

    public static void main(String[] args) {
        // WHILE LOOP FOR COUNT 1 TO 10 LOVE YOU JAAN;

        int love = 1;
        while (love <= 5) {
            System.out.println("I LOVE YOU WHILE LOOP JAAN - " + love);
            love++;
        }
        // do while
        do {
            System.out.println("I LOVE YOU WHILE DO JAAN - " + love);
            love++;
        } while (love <= 10);

        // for loop
        for (int l = 1; l <= 5; l++) {
            System.out.println("I LOVE YOU FOR LOOP JAAN - " + l);

        }

    }

}
